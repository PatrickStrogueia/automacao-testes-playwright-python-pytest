# Inteiro (int)

status_code = 200
quantidade_usuarios = 15
tentativas = 3

print(status_code)
print(quantidade_usuarios)
print(tentativas)

# Ponto flutuante (float)

tempo_resposta = 1.42
valor_total = 199.90

print(tempo_resposta)
print(valor_total)

# Texto (str)

mensagem = "Usuário criado com sucesso"
endpoint = "/api/login"
nome_usuario = "admin"

print(mensagem)
print(endpoint)
print(nome_usuario)

# Booleano (bool)

login_valido = True
usuario_ativo = False

print(login_valido)
print(usuario_ativo)

# Valor nulo (None)

token = None
print(token)

# Descobrindo o tipo de uma variável

tempo = 1.5
print(type(tempo))

mensagem = "Ok"
print(type(mensagem))

# Cuidados iniciais

codigo = 200
codigo_texto = "200"

print(codigo)
print(codigo_texto)

print(codigo == codigo_texto)
